#include <iostream>
using namespace std;
int main()
{
    int a,b;
    cout<<"enter the value of a";
    cin>> a;
    cout<<"enter the value of b";
    cin>>b;
    cout<<"the value of bitwise opertator AND is "<<(a&b)<<endl;
    cout<<"the value of bitwise opertator OR is "<<(a|b)<<endl;
    cout<<"the value of bitwise opertator XOR is "<<(a^b)<<endl;
    cout<<"the value of bitwise opertator NOT is "<<(~a)<<endl;
    cout<<"the value of the bitwise operator left shift is "<<(a<<(2))<<endl;
    cout<<"the value of the bitwise operator left shift is "<<(b<<(1))<<endl;
    cout<<"the value of the bitwise operator  right shift is "<<(a>>(1))<<endl;
    cout<<"the value of the bitwise operator  right shift is "<<(b<<(2))<<endl;

}